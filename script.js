function myfunction1(){
			var x= document.getElementById("frame");
			var y= document.getElementById("lyricsdiv");

			document.getElementById("sketch").style.display = 'block';
			for (var z=1;z<5;z++){
				document.getElementById("lyrics" + z).style.display = 'none';
			}
		
			x.style.width = '60%';
			x.style.float = 'left';
			y.style.float = 'left';
			y.style.margin = '30px';
			y.style.color= 'grey';
			document.getElementById("lyrics1").style.display = 'block';
			}
			
